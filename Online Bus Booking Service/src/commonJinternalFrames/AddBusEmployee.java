    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow() < 0){
            jTextFieldCompany.setText("");
            jTextFieldReg.setText("");
            jTextFieldSeats.setText("");
            jCheckBoxAC.setSelected(false);
            jCheckBoxActive.setSelected(false);
        }
        else{
            int rowKey = jTable1.getSelectedRow();
            jTextFieldCompany.setText((dm.getValueAt(rowKey, 0)).toString());
            jTextFieldReg.setText((dm.getValueAt(rowKey, 1)).toString());
            jTextFieldSeats.setText((dm.getValueAt(rowKey, 2)).toString());
            
            if(Boolean.valueOf(dm.getValueAt(rowKey, 3).toString())==null){
                jCheckBoxAC.setSelected(false);
            }else if(Boolean.valueOf(dm.getValueAt(rowKey, 3).toString())==true){
                jCheckBoxAC.setSelected(true);
            }else{
                jCheckBoxAC.setSelected(false);
            }
            if(Boolean.valueOf(dm.getValueAt(rowKey, 4).toString())==null){
                jCheckBoxActive.setSelected(false);
            }else if(Boolean.valueOf(dm.getValueAt(rowKey, 4).toString())==true){
                jCheckBoxActive.setSelected(true);
            }else{
                jCheckBoxActive.setSelected(false);
            }
            jTextFieldFare.setText(dm.getValueAt(rowKey, 6).toString());
            jTextFieldFareExtended.setText(dm.getValueAt(rowKey, 7).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

