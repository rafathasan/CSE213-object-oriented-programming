    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showOptionDialog(null, "Current Data will be replaced", "Overwrite Data", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, null, null, null) == JOptionPane.OK_OPTION){
            new SwingWorker(){

                    float progress=0;
                    JProgressBar bar = new JProgressBar();
                    JOptionPane pane = new JOptionPane(bar);
                    JDialog dialLog = pane.createDialog(null, "Loading");

                    @Override
                    protected void done() {
                        dialLog.setTitle("Done");
                        jTable1.clearSelection();
                        jTable1.setVisible(true);
                        new SwingWorker() {
                            @Override
                            protected Object doInBackground() throws Exception {
                                Thread.sleep(5000);
                                dialLog.dispose();
                                return null;
                            }
                        }.execute();
                    }

                    @Override
                    protected void process(List list) {

                        progress = Float.parseFloat(list.get(list.size()-1).toString());

                        bar.setValue((int) progress);
                    }

                    @Override
                    protected Object doInBackground(){

                        jTable1.setVisible(false);

                    /////////////////////////////Editable:START//////////////////////////////
                    ArrayList<Bus> list = new ArrayList<Bus>();
                    for(int i=0;i<jTable1.getRowCount();i++){
                        list.add(new Bus(jTable1.getValueAt(i, 0).toString(), jTable1.getValueAt(i, 1).toString(), Integer.parseInt(jTable1.getValueAt(i, 2).toString()), Boolean.parseBoolean(jTable1.getValueAt(i, 3).toString()), Boolean.parseBoolean(jTable1.getValueAt(i, 4).toString()), jTable1.getValueAt(i, 5).toString(), Integer.valueOf(jTable1.getValueAt(i, 6).toString()), Float.valueOf(jTable1.getValueAt(i, 7).toString()) ));

                        publish( ((float) 100/jTable1.getRowCount()) + progress);////////progressBar///////////////

                        try{
                            Thread.sleep(5);
                        }catch(Exception ex){
                        }
                    }
                    publish(100);
                    /////////////////////////////Editable:END//////////////////////////////

                        new SwingWorker(){
                            @Override
                            protected Object doInBackground() throws Exception {
                                dialLog.setModal(false);
                                dialLog.setLocation(GraphicsEnvironment.getLocalGraphicsEnvironment().getDefaultScreenDevice().getDisplayMode().getWidth() - dialLog.getSize().width,
                                        GraphicsEnvironment.getLocalGraphicsEnvironment().getDefaultScreenDevice().getDisplayMode().getHeight() 
                                                - dialLog.getSize().height 
                                                - (Toolkit.getDefaultToolkit().getScreenSize().height 
                                                        - GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds().height) );
                                dialLog.setVisible(true);
                                return null;
                            }
                        }.execute();

                        /////////////////////////////Editable:START//////////////////////////////
                        try{
                            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(new File("BUS")));
                            oos.writeObject(list);
                            oos.flush();
                            oos.close();
                        }catch(Exception ex){
                            if(JOptionPane.showOptionDialog(null, "Send Error report", "File write Exception", JOptionPane.OK_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null)==JOptionPane.OK_OPTION){
                                    SystemErrorHandle.reportError(ex);
                                }
                        }
                        /////////////////////////////Editable:END//////////////////////////////
                        return true;
                    }

                }.execute();
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        new SwingWorker(){
            
                float progress=0;
                JProgressBar bar = new JProgressBar();
                JOptionPane pane = new JOptionPane(bar);
                JDialog dialLog = pane.createDialog(null, "Loading");
                        
                @Override
                protected void done() {
                    dialLog.setTitle("Done");
                    jTable1.clearSelection();
                    jTable1.setVisible(true);
                    new SwingWorker() {
                        @Override
                        protected Object doInBackground() throws Exception {
                            Thread.sleep(5000);
                            dialLog.dispose();
                            return null;
                        }
                    }.execute();
                }

                @Override
                protected void process(List list) {
                    
                    progress = Float.parseFloat(list.get(list.size()-1).toString());
                    
                    bar.setValue((int) progress);
                }

                @Override
                protected Object doInBackground(){
                    
                    jTable1.setVisible(false);
                    
                /////////////////////////////Editable:START//////////////////////////////
                    ArrayList<Bus> list;
                try{
                    ObjectInputStream ois = new ObjectInputStream(new FileInputStream(new File("BUS")));
                    list = (ArrayList<Bus>) ois.readObject();
                    ois.close();
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null, "Data Retrive Failed");
                    return false;
                }
                /////////////////////////////Editable:END//////////////////////////////
                    
                    new SwingWorker(){
                        @Override
                        protected Object doInBackground() throws Exception {
                            dialLog.setModal(false);
                            dialLog.setLocation(GraphicsEnvironment.getLocalGraphicsEnvironment().getDefaultScreenDevice().getDisplayMode().getWidth() - dialLog.getSize().width,
                                    GraphicsEnvironment.getLocalGraphicsEnvironment().getDefaultScreenDevice().getDisplayMode().getHeight() 
                                            - dialLog.getSize().height 
                                            - (Toolkit.getDefaultToolkit().getScreenSize().height 
                                                    - GraphicsEnvironment.getLocalGraphicsEnvironment().getMaximumWindowBounds().height) );
                            dialLog.setVisible(true);
                            return null;
                        }
                    }.execute();
                    
                    /////////////////////////////Editable:START//////////////////////////////
                    DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                    dm.setRowCount(0);
                    for(int i=0;i<list.size();i++){
                        dm.addRow(new Object[]{list.get(i).getCompany(),list.get(i).getRegNo(), list.get(i).getSeats(), list.get(i).isAc(), list.get(i).isInService(), list.get(i).getId(), list.get(i).getSeatFare(), list.get(i).getFareExtendedRatio()});
                        
                        publish( ((float) 100/list.size()) + progress);////////progressBar///////////////
                        
                        try{
                            Thread.sleep(5);
                        }catch(Exception ex){
                            if(JOptionPane.showOptionDialog(null, "Create Error Report", "Thread Inturupt Exception", JOptionPane.OK_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null)==JOptionPane.OK_OPTION){
                                SystemErrorHandle.reportError(ex);
                            }
                        }
                    }
                    /////////////////////////////Editable:END//////////////////////////////
                    publish(100);
                    jTable1.setModel(dm);
                    return true;
                }
                
            }.execute();
    }//GEN-LAST:event_jMenuItem1ActionPerformed

